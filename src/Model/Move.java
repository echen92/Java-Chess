package Model;

/**
 * Represents a move on the board
 */
public class Move {
    private int startX, startY;
    private int endX, endY;

    /**
     * Constructor for a move
     * @param srcX Starting X coord
     * @param srcY Starting Y coord
     * @param destX Ending X coord
     * @param destY Ending Y coord
     */
    public Move(int srcX, int srcY, int destX, int destY) {
        startX = srcX;
        startY = srcY;
        endX = destX;
        endY = destY;
    }

    /**
     * Gets starting X position
     * @return starting X coord
     */
    public int getStartX() { return startX; }

    /**
     * Gets starting Y position
     * @return starting Y coord
     */
    public int getStartY() { return startY; }

    /**
     * Gets ending X position
     * @return ending X coord
     */
    public int getEndX() { return endX; }

    /**
     * Gets ending Y position
     * @return ending Y coord
     */
    public int getEndY() { return endY; }


}
